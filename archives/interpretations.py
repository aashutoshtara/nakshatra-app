# HELPER FUNCTIONS
def get_planet_in_sign(planet: str, sign: str) -> dict:
    return PLANET_IN_SIGN.get(planet, {}).get(sign, {})

def get_house_lord_placement(from_house: int, to_house: int) -> dict:
    key = f"House{from_house}LordInHouse{to_house}"
    return HOUSE_LORDS.get(key, {})

def get_planet_in_house(planet: str, house: int) -> dict:
    return PLANET_IN_HOUSE.get(planet, {}).get(f"House{house}", {})

def get_rising_sign(sign: str) -> dict:
    return RISING_SIGN.get(sign, {})

def get_yogas_by_nature(nature: str = None) -> list:
    if nature:
        return [y for y in YOGAS if y.get("nature") == nature]
    return YOGAS

def search_yogas(keyword: str) -> list:
    keyword = keyword.lower()
    return [y for y in YOGAS if keyword in y.get("name", "").lower() or keyword in y.get("description", "").lower()]

def get_dasha_effect(sign: str, planet: str, level: int = 1) -> dict:
    key = f"{sign}_{planet}_PD{level}"
    return DASHA_EFFECTS.get(key, {})

def get_muhurtha_advice(category: str) -> list:
    return MUHURTHA.get(category, [])
